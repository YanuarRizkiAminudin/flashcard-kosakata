# Read existing CSV
lines = open('verb_data.csv', encoding='utf-8').readlines()

new_lines = []
for line in lines:
    line = line.rstrip('\n')
    if not line.strip():
        new_lines.append(line)
        continue
    parts = line.split(',', 8)
    if len(parts) < 7:
        new_lines.append(line)
        continue
    v1 = parts[0].strip().lstrip('\ufeff')
    trans = translations.get(v1, '')
    # Check if translation already added (9 columns)
    if len(parts) >= 9:
        new_lines.append(line)  # already has translation
    else:
        new_lines.append(line + ',' + trans)

open('verb_data.csv', 'w', encoding='utf-8', newline='\n').writelines('\n'.join(new_lines) + '\n')
print(f'Done! {len([l for l in new_lines if l.strip()])} verbs updated')
